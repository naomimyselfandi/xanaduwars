package io.github.naomimyselfandi.xanaduwars.gameplay.common;

import io.github.naomimyselfandi.xanaduwars.ext.ExcludeFromCoverageReport;

/// A direction on the game map.
@ExcludeFromCoverageReport
public enum Direction {

    /// One step along the negative Y-axis.
    NORTH,

    /// One step along the positive X-axis.
    EAST,

    /// One step along the positive Y-axis.
    SOUTH,

    /// One step along the negative X-axis.
    WEST,

}
